package aula01;

public class Fibonacci {
	
	public static void main(String[] args) {
		Integer numero1 = 0;
		Integer numero2 = 1;
		
		Integer result = 0;
		
		
		for(int i = 0; i <= 100; i++) {
			System.out.println(result);
			numero1 = numero2;
			numero2 = result;
			result = numero1 + numero2;
		}
	}
}
